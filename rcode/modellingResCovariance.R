##### Modelling the residual covariance structure


library(expm)
H <- matrix(0, 20, 20)
for(i in 0:19){
  if(i==19){
    H[i+1, ] <- c(i:0)
  }else{
    H[i+1, ] <- c(i:0, 1:(19-i))
  }
}
sqrtm(exp(-H^2))